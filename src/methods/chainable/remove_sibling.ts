import { Chainable } from './Chainable'

export function remove_sibling(a: Chainable, order: 'right' | 'left'): void {
  const right = order
  const left = order == 'left' ? 'right' : 'left'
  if (a[right]) {
    const b = a[right]
    a[right] = b[right]
    if (b[right]) {
      b[right][left] = a
    }
    b[left] = undefined
    b[right] = undefined
  }
}
